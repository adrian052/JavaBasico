
public class Vertice {
	private int x, y;
//constructor
	public Vertice(int x,int y){
		this.x= x;
		this.y= y;
	}
	
	
	
//	set and get
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
